export const summaryOfTwoNumbers = (firstNum: number, secondNum: number) => {
  return Number((firstNum + secondNum).toFixed(2))
}

export const subtractionOfTwoNumbers = (firstNum: number, secondNum: number) => {
  return Number((firstNum - secondNum).toFixed(2))
}

export const multiplicationOfTwoNumber = (firstNum: number, secondNum: number) => {
  return Number((firstNum * secondNum).toFixed(2))
}